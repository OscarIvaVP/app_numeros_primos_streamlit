import streamlit as st

def es_primo(numero):
    """
    Funci√≥n para determinar si un n√∫mero es primo.
    Un n√∫mero primo es un n√∫mero natural mayor que 1 que no tiene divisores positivos
    m√°s que 1 y √©l mismo.
    """
    # Los n√∫meros primos deben ser mayores que 1.
    if numero <= 1:
        return False

    # Se itera desde 2 hasta la ra√≠z cuadrada del n√∫mero.
    # Si se encuentra un divisor en este rango, el n√∫mero no es primo.
    for i in range(2, int(numero**0.5) + 1):
        if numero % i == 0:
            return False

    # Si no se encontraron divisores, el n√∫mero es primo.
    return True

# --- Interfaz de la aplicaci√≥n Streamlit ---

# T√≠tulo de la aplicaci√≥n que se mostrar√° en la web.
st.title("üî¢ Verificador de N√∫meros Primos")

# Descripci√≥n o subt√≠tulo.
st.write("Esta aplicaci√≥n te ayuda a identificar si un n√∫mero entero es primo o no.")

# Campo de entrada para que el usuario ingrese un n√∫mero.
# `min_value=0` asegura que no se ingresen n√∫meros negativos.
# `step=1` permite que el n√∫mero se incremente de uno en uno.
numero_usuario = st.number_input("Ingresa un n√∫mero entero:", min_value=0, step=1)

# Bot√≥n para ejecutar la verificaci√≥n.
# El c√≥digo dentro de este 'if' solo se ejecutar√° cuando el usuario haga clic en el bot√≥n.
if st.button("Verificar si es primo"):
    # Llama a la funci√≥n 'es_primo' con el n√∫mero del usuario.
    if es_primo(numero_usuario):
        # Si la funci√≥n devuelve True, muestra un mensaje de √©xito.
        st.success(f"¬°El n√∫mero {numero_usuario} S√ç es un n√∫mero primo! üëç")
    else:
        # Si la funci√≥n devuelve False, muestra un mensaje de error/advertencia.
        st.error(f"El n√∫mero {numero_usuario} NO es un n√∫mero primo. üëé")

